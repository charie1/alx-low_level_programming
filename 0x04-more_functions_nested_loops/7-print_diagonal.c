#include "main.h"
/**
 * print_diagonal - checks the digit typed.
 * @n: the num to be typed
 * @y: the line to be drawn
 * Return: Success(0).
 */

void print_diagonal(int n)
{
	int y;

	if (n > 0)
	
	{
		for (y = 0; y = n; y++)
										_putchar ('#');
								}

	else
					                		_putchar('\n');
}
